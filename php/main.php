            <h2>
              Introduction
            </h2>
            <br />
            <p>
              The Tarantula Hawk
              <i>(pepsis grossa, formerly pepsis formosa)</i> is an
              extraordinary insect. It’s a large and menacing solitary wasp,
              somewhat different than the typical yellowjacket. First of all,
              it’s much, much larger. They can grow up to 7.5 centimeters in
              length and a wingspan of 10 centimeters. On some females, the
              stinger alone can reach lengths of 7mm. Another unusual trait of
              Pepsis grossa is that they have an abnormally long lifespan for a
              wasp. They are known to live for up to one full year. Quite
              peculiar indeed.
            </p>
            <br />
            <p>
              There’s surprisingly little known about this Tarantula Hawk,
              perhaps because the painful sting makes it somewhat unencouraging
              to study. Generally however, they won’t attack unless they feel
              threatened, so slow movements and gentle handling can help you
              avoid one of the most painful stings the animal kingdom has to
              offer. In order to understand them better, we’re going to look a
              little bit at their family.
            </p>
