            <h2>A little about wasps</h2>
            <p>
              Wasps are interesting. They are not a formal group in and of
              themselves, but rather an exclusion from the rest of their
              suborder. Their suborder, Apocrita, also includes ants and bees.
              What’s interesting about wasps is that they are defined as any
              insect in the order of Hymenoptera, in the suborder of Apocrita,
              that is NOT an ant or a bee. So, any insect in their suborder that
              isn’t defined as either an ant or a bee, is automatically a wasp.
            </p>
            <br />
            <p>
              Tarantula Hawks are part of the Pompilidae family, the spider wasp
              family. This is a very large family of parasitic and solitary
              wasps, either feeding on spiders or laying their eggs inside them.
              There are over 5000 wasps in the Pompilidae family, and their
              distribution is worldwide.
            </p>
