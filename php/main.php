            <h2>Diet</h2>
            <p>
              Believe it or not, but the adult tarantula hawk is a nectarivore.
              The ultimate form of veganism, nectarivores feed exclusively on
              nectar. It’s a common belief that only monarch butterflies feed on
              milkweed nectar, but tarantula hawks are known to primarily feed
              on milkweed nectar as well. They do occasionally dabble in other
              forms of food, however, and have been known to drink themselves
              silly on the juice of fermented fruit.
            </p>
            <br />
            <p>
              The parasitic young, however, feed voraciously on the tarantula
              their egg was laid in. They hatch from their egg inside the
              abdomen of the tarantula and begin eating the tarantula from the
              inside out, avoiding vital organs in order to keep the tarantula
              alive. Ideally, the tarantula only dies once the wasp is at the
              final stage of its development, right before it leaves the burrow.
            </p>
